class UserAgentRotator:
    """Thread-safe User-Agent rotator with configurable rotation interval."""

    def __init__(self, browser_type: str = "random", rotate_every: int = 10) -> None:
        self._browser_type = browser_type
        self._rotate_every = rotate_every
        self._request_count = 0
        self._current_ua: str | None = None
        self._lock = threading.Lock()

    def get(self) -> str:
        """Return current User-Agent, rotating when rotation interval is reached."""
        with self._lock:
            self._request_count += 1
            if self._current_ua is None or self._request_count % self._rotate_every == 0:
                self._current_ua = self._pick_random()
            return self._current_ua

    def _pick_random(self) -> str:
        if self._browser_type == "random":
            pool = [ua for uas in _USER_AGENTS.values() for ua in uas]
        else:
            pool = _USER_AGENTS.get(self._browser_type, list(_USER_AGENTS["chrome"]))
        return random.choice(pool)

    @property
    def all_user_agents(self) -> dict[str, list[str]]:
        """Return the full UA dictionary (read-only view)."""
        return dict(_USER_AGENTS)

    @property
    def total_count(self) -> int:
        """Return total number of available User-Agent strings."""
        return sum(len(v) for v in _USER_AGENTS.values())
